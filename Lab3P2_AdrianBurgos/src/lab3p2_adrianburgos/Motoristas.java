package lab3p2_adrianburgos;

public class Motoristas extends Empleados{
    private int comisiones;
    private String transporte;
    private int maxencargos;

    public int getComisiones() {
        return comisiones;
    }

    public void setComisiones(int comisiones) {
        this.comisiones = comisiones;
    }

    public String getTransporte() {
        return transporte;
    }

    public void setTransporte(String transporte) {
        this.transporte = transporte;
    }

    public int getMaxencargos() {
        return maxencargos;
    }

    public void setMaxencargos(int maxencargos) {
        this.maxencargos = maxencargos;
    }
    
}
